class FutureInsight {
  final String id;
  final InsightCategory category;
  final InsightSource source;
  final String scientificFactRu;
  final String scientificFactKk;
  final String motherTextRu;
  final String motherTextKk;
  final String sourceUrl;
  final int minAgeMonths; // –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –≤–æ–∑—Ä–∞—Å—Ç —Ä–µ–±–µ–Ω–∫–∞
