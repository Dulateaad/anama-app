  final List<String> relatedTaskKeywords; // –ö–ª—é—á–µ–≤—ã–µ —Å–ª–æ–≤–∞ –¥–ª—è —Å–≤—è–∑–∏ —Å –∑–∞–¥–∞–Ω–∏—è–º–∏

  const FutureInsight({
    required this.id,
    required this.category,
    required this.source,
    required this.scientificFactRu,
    required this.scientificFactKk,
    required this.motherTextRu,
    required this.motherTextKk,
    required this.sourceUrl,
    this.minAgeMonths = 0,
    this.maxAgeMonths = 60,
    this.relatedTaskKeywords = const [],
  });

  String getScientificFact(String languageCode) {
    return languageCode == 'kk' ? scientificFactKk : scientificFactRu;
  }

  String getMotherText(String languageCode) {
    return languageCode == 'kk' ? motherTextKk : motherTextRu;
  }

  String get sourceLabel {
    switch (source) {
      case InsightSource.harvard:
        return 'Harvard Center on the Developing Child';
      case InsightSource.upToDate:
        return 'UpToDate Clinical Standards';
    }
  }

  String get learnMoreUrl {
    switch (source) {
      case InsightSource.harvard:
        return 'https://developingchild.harvard.edu/';
      case InsightSource.upToDate:
        return 'https://www.uptodate.com/';
    }
  }
}

